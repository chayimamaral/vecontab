CREATE TABLE IF NOT EXISTS public.ibge_cnae_secao (
    id        smallserial PRIMARY KEY,
    nome      text NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS public.ibge_cnae_divisao (
    id        serial PRIMARY KEY,
    secao_id  smallint NOT NULL REFERENCES public.ibge_cnae_secao (id) ON DELETE CASCADE,
    nome      text NOT NULL,
    UNIQUE (secao_id, nome)
);

CREATE TABLE IF NOT EXISTS public.ibge_cnae_grupo (
    id           serial PRIMARY KEY,
    divisao_id   int NOT NULL REFERENCES public.ibge_cnae_divisao (id) ON DELETE CASCADE,
    nome         text NOT NULL,
    UNIQUE (divisao_id, nome)
);

CREATE TABLE IF NOT EXISTS public.ibge_cnae_classe (
    id         serial PRIMARY KEY,
    grupo_id   int NOT NULL REFERENCES public.ibge_cnae_grupo (id) ON DELETE CASCADE,
    nome       text NOT NULL,
    UNIQUE (grupo_id, nome)
);

CREATE TABLE IF NOT EXISTS public.ibge_cnae_subclasse (
    id          serial PRIMARY KEY,
    classe_id   int NOT NULL REFERENCES public.ibge_cnae_classe (id) ON DELETE CASCADE,
    codigo      char(7) NOT NULL UNIQUE,
