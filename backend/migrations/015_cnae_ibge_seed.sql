INSERT INTO public.cnae (secao, divisao, grupo, classe, subclasse, denominacao, ativo)
SELECT s.nome, d.nome, g.nome, c.nome, sc.codigo::text, sc.nome, true
FROM public.ibge_cnae_subclasse sc
JOIN public.ibge_cnae_classe c ON c.id = sc.classe_id
JOIN public.ibge_cnae_grupo g ON g.id = c.grupo_id
JOIN public.ibge_cnae_divisao d ON d.id = g.divisao_id
JOIN public.ibge_cnae_secao s ON s.id = d.secao_id
ORDER BY sc.codigo;

SELECT setval(pg_get_serial_sequence('public.ibge_cnae_secao', 'id'), COALESCE((SELECT MAX(id) FROM public.ibge_cnae_secao), 1));
SELECT setval(pg_get_serial_sequence('public.ibge_cnae_divisao', 'id'), COALESCE((SELECT MAX(id) FROM public.ibge_cnae_divisao), 1));
SELECT setval(pg_get_serial_sequence('public.ibge_cnae_grupo', 'id'), COALESCE((SELECT MAX(id) FROM public.ibge_cnae_grupo), 1));
SELECT setval(pg_get_serial_sequence('public.ibge_cnae_classe', 'id'), COALESCE((SELECT MAX(id) FROM public.ibge_cnae_classe), 1));
SELECT setval(pg_get_serial_sequence('public.ibge_cnae_subclasse', 'id'), COALESCE((SELECT MAX(id) FROM public.ibge_cnae_subclasse), 1));

